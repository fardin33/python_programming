
# Take two inputs from the user. One will be an integer. The other will be a float number. Then multiply them to display the output.
# ব্যবহারকারীর কাছ থেকে দুটি ইনপুট নিন। একটি একটি পূর্ণসংখ্যা হবে. অন্যটি একটি ফ্লোট নম্বর হবে। তারপর আউটপুট প্রদর্শন করতে তাদের গুণ করুন।

input_value_One = int(input("Input Your Integer Number: "))

